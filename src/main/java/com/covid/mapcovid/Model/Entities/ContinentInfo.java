package com.covid.mapcovid.Model.Entities;

public class ContinentInfo {


	   
	    private int lat;
	    private int Long;
	   

	    public ContinentInfo() {
	    }

	    public ContinentInfo(int lat, int aLong) {
	      
	        this.lat = lat;
	        Long = aLong;
	       
	    }

	    public int getLat() {
	        return lat;
	    }

	    public void setLat(int lat) {
	        this.lat = lat;
	    }

	    public int getLong() {
	        return Long;
	    }

	    public void setLong(int aLong) {
	        Long = aLong;
	    }

	
	
		}
	    
	    

	

